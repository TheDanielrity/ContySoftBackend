        self.precio_unitario = precio_unitario
        self.codigo_item = codigo_item
        self.codigo_unidad_medida = codigo_unidad_medida
        self.unidad_medida_desc = unidad_medida_desc
        self.valor_vta_unitario = valor_vta_unitario
        self.fecha_emision = fecha_emision
